8.4 ข้อเรียกร้องภายใต้ส่วนที่ 8 นี้ต้องจัดส่งให้ตามแบบฟอร์มที่กำหนดโดย Moldii App ซึ่งอาจจะมีการปรับปรุงเป็นครั้งคราว และอย่างน้อยต้องดำเนินการดังต่อไปนี้ด้วย (ก) ลงลายมือชื่อจริงหรือลายเซ็นอิเล็กทรอนิกส์ของเจ้าของ IPR หรือ ตัวแทน IPR (เรียกรวมกันว่า “ผู้ให้ข้อมูล”) (ข) คำอธิบายถึงประเภทและลักษณะของสิทธิในทรัพย์สินทางปัญญาที่ถูกกล่าวหาว่าถูกละเมิดและหลักฐานแห่งสิทธิต่างๆ (ค)คำอธิบายลักษณะของการละเมิดที่ถูกกล่าวหาพร้อมรายละเอียดที่เพียงพอเพื่อให้ Moldii App สามารถประเมินข้อร้องเรียนได้ (ง) URLของรายชื่อที่มีการกล่าวหาว่าละเมิด (จ) ข้อมูลเพียงพอที่จะช่วยให้ Moldii App ติดต่อผู้ให้ข้อมูลได้ เช่น ที่อยู่ทางไปรษณีย์ หมายเลขโทรศัพท์ และที่อยู่อีเมล (ฉ) ถ้อยแถลงจากผู้ให้ข้อมูลที่ว่าข้อร้องเรียนนี้ได้ยื่นเข้ามาด้วยความเชื่อโดยสุจริตและการใช้ทรัพย์สินทางปัญญาที่ระบุโดยผู้ให้ข้อมูล ไม่ได้รับอนุญาตจากเจ้าของ IPR หรือตามที่กฎหมายบัญญัติให้ดำเนินการได้ (ช) ถ้อยแถลงจากผู้ให้ข้อมูลที่ว่าข้อมูลในหนังสือบอกกล่าวนั้นถูกต้อง ทั้งนี้ผู้ให้ข้อมูลจะต้องรับผิดชดใช้ค่าเสียหายที่เราอาจได้รับอันเป็นผลมาจากข้อมูลที่ผู้ให้ข้อมูลได้จัดเตรียมมาให้ นอกจากนี้ ผู้ให้ข้อมูลรับรองว่าตนมีสิทธิอย่างเหมาะสมหรือได้รับอนุญาตให้ดำเนินการในนามของเจ้าของ IPR ในทุกกรณีที่เกี่ยวข้องกับการร้องเรียนดังกล่าว
<br>
8.5 นอกจากกรณีที่มีการละเมิดสิทธิสิทธิในทรัพย์สินทางปัญญาตามที่ระบุข้างต้น Moldii App ไม่มีหน้าที่ในการให้ความช่วยเหลือหรือดำเนินการใดๆ ในการบังคับใช้ข้อตกลงทางการค้าระหว่างผู้ผลิตและผู้ขาย รวมถึงเรื่องสิทธิในการจัดจำหน่ายแต่เพียงผู้เดียว และเรื่องการควบคุมราคา 
<br>
8.6 ผู้ขายแต่ละรายและทุกรายตกลงที่จะไม่ให้ Moldii App และบริษัทในเครือ รับผิดชอบจากข้อเรียกร้องทั้งหมด จากการกระทำจากความเสียหายและการตัดสินในการนำเนื้อหาหรือรายการผลิตภัณฑ์ใด ๆ ออก อันเกิดจากการเรียกร้องเกี่ยวกับการละเมิดทรัพย์สินทางปัญญาใด ๆ























