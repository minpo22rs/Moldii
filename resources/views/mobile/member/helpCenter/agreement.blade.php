<u>9. การสั่งซื้อและการชำระเงิน</u>
<br>
9.1 Moldii App อาจจัดให้มีการชำระเงินด้วยวิธีการต่างๆ รวมถึงวิธีการซึ่งระบุไว้ต่อไปนี้ ทั้งนี้ Moldii App สงวนสิทธิแต่เพียงผู้เดียวที่จะแก้ไข ระงับ หรือยกเลิกวิธีการชำระเงินประเภทใด ๆ โดยไม่ต้องแจ้งให้ทราบ
<br>
(1) บัตรเครดิต
การชำระผ่านบัตรเครดิตจะดำเนินการผ่านช่องทางการชำระเงินของบุคคลภายนอก และชนิดของบัตรเครดิตที่ช่องทางการชำระเงินเหล่านี้ยอมรับอาจแตกต่างกันไปตามเขตอำนาจตามกฎหมายที่คุณอยู่
<br>
(2) การชำระเงินสดเมื่อรับสินค้า (Cash on Delivery, COD)
Moldii App ได้จัดให้มีบริการรับชำระเงินสดเมื่อรับสินค้าสำหรับการให้บริการในบางประเทศ ผู้ซื้ออาจชำระเงินสดโดยตรงให้แก่ตัวแทนขนส่งสินค้า ณ เวลาที่ผู้ซื้อรับสินค้าที่สั่งซื้อ
<br>
(3) การโอนเงินผ่านธนาคาร
ผู้ซื้อสามารถชำระเงินผ่านเครื่องเอทีเอ็ม QR พร้อมเพย์ หรือโอนเงินผ่านธนาคารทางอินเทอร์เน็ต (“การโอนเงินผ่านธนาคาร”) ไปยังบัญชี Moldii App ที่เรากำหนดไว้ ผู้ซื้อจะต้องส่งใบเสร็จการรับโอนเงินหรือหมายเลขอ้างอิงการทำรายการชำระเงินให้แก่ Moldii App เพื่อทำการตรวจสอบผ่านฟังก์ชั่น “อัปโหลดใบเสร็จ” ที่อยู่ในแอปของ Moldii App เพื่อเป็นการยืนยันการชำระเงิน หาก Moldii App ไม่ได้รับการยืนยันการชำระเงินภายในสาม (3) วัน คำสั่งซื้อของผู้ซื้อจะถูกยกเลิก
<br>
(4) กระเป๋าเงินอิเล็กทรอนิกส์ (E-Wallet)
คุณจะได้รับบริการนี้จาก Moldii App โดยเราจะให้บริการดังกล่าวร่วมกันกับ บริษัท เพลินพอดี จำกัด (“MoldiiPay”) หรือผู้ให้บริการอื่นใดที่เกี่ยวข้องของ Moldii App ตามเงื่อนไข ข้อกำหนดและนโยบายการใช้บริการที่ MoldiiPay หรือผู้ให้บริการที่เกี่ยวข้องเป็นผู้กำหนด หากคุณได้ลงทะเบียนหรือเปิดใช้บัญชีกระเป๋าเงินอิเล็กทรอนิกส์และใช้บริการของ Moldii App ให้ถือว่าคุณได้ยอมรับเงื่อนไข ข้อกำหนดและนโยบายการใช้บริการที่ MoldiiPay หรือผู้ให้บริการที่เกี่ยวข้องกำหนดไว้แล้ว และคุณยินยอมให้ Moldii App ทำการโอนถ่ายข้อมูลส่วนบุคคลของคุณที่ในความดูแลของ Moldii App ให้แก่ MoldiiPay หรือผู้ให้บริการที่เกี่ยวข้องเพื่อใช้ในการเปิดใช้หรือลงทะเบียนบัญชีกระเป๋าเงินอิเล็กทรอนิกส์ การจัดการ ดำเนินการ บริหาร และให้บริการกระเป๋าเงินอิเล็กทรอนิกส์อื่นใดให้แก่คุณ รวมถึงเพื่อวัตถุประสงค์อื่นใดที่เกี่ยวข้องและที่เกี่ยวเนื่องกับการให้บริการดังกล่าว
