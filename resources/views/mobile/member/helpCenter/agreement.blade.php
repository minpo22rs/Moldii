15.1 บริการนี้จัดไว้ให้ “ตามสภาพจริง” โดยไม่มีการรับประกัน การอ้างสิทธิ์ หรือการรับรองโดย Moldii App ไม่ว่าในลักษณะใด ทั้งแบบชัดแจ้ง โดยนัย หรือแบบบังคับ อันเกี่ยวเนื่องกับบริการ รวมถึงแต่ไม่จำกัดเพียง การรับประกันคุณภาพ การดำเนินงาน การไม่ละเมิดสิทธิ ความสามารถในการทำการค้า หรือความเหมาะสมสำหรับวัตถุประสงค์เฉพาะ รวมทั้งไม่มีการรับประกันที่เกิดขึ้นระหว่างการติดต่อ การดำเนินงาน หรือการใช้เพื่อการค้า โดยไม่เป็นการจำกัดสิ่งที่กล่าวไปก่อนหน้านี้ และจนถึงขอบเขตสูงสุดที่กฎหมายที่เกี่ยวข้องอนุญาตไว้ Moldii App ไม่รับประกันว่า บริการ ไซต์นี้ หรือการทำงานในไซต์จะพร้อมใช้งานได้ เข้าถึงได้ ไม่หยุดชะงัก ทันต่อเวลา ปลอดภัย ถูกต้อง ครบถ้วน หรือปราศจากข้อผิดพลาด ว่าข้อบกพร่อง หากมี จะได้รับการแก้ไข หรือว่า ไซต์นี้และ/หรือเซิร์ฟเวอร์ของไซต์จะใช้งานได้โดยปราศจากไวรัส นาฬิกา ระบบตั้งเวลา ตัวนับ หนอน ล็อคซอฟต์แวร์ อุปกรณ์สอดแนม ม้าโทรจัน เราติ้ง ประตูกับดัก ระเบิดเวลา หรือรหัส คำสั่ง โปรแกรม หรือส่วนประกอบที่เป็นอันตรายอื่นใด
<br>
15.2 คุณรับทราบว่าความเสี่ยงทั้งหมดที่เกิดขึ้นจากการใช้หรือการดำเนินงานของไซต์นี้และ/หรือบริการ ตกเป็นของคุณจนถึงขอบเขตสูงสุดที่กฎหมายที่เกี่ยวข้องอนุญาตไว้
<br>
15.3 Moldii App ไม่ได้ควบคุมและ จนถึงขอบเขตสูงสุดที่กฎหมายที่เกี่ยวข้องอนุญาตไว้ ไม่รับประกันหรือรับผิดชอบต่อ: (ก) ความเหมาะสมเพื่อวัตถุประสงค์ การมีอยู่ คุณภาพ ความปลอดภัยหรือความชอบด้วยกฎหมายของสินค้าที่วางจำหน่ายผ่านทางบริการ หรือ (ข) ความสามารถของผู้ขายในการขายสินค้าหรือของผู้ซื้อในการชำระค่าสินค้า หากมีข้อพิพาทที่เกี่ยวกับผู้ใช้ตั้งแต่หนึ่งคนขึ้นไป ผู้ใช้ดังกล่าวตกลงที่จะยุติข้อพิพาทระหว่างกันโดยตรง และจนถึงขอบเขตสูงสุดที่กฎหมายที่เกี่ยวข้องอนุญาตไว้ ปลดปล่อย Moldii App และบริษัทในเครือออกจากการอ้างสิทธิ การเรียกร้อง และความเสียหายใดๆ และทั้งหมด อันเกิดจากหรือเกี่ยวเนื่องกับข้อพิพาทดังกล่าว







<br>
<u>16. ข้อยกเว้นและข้อจำกัดการรับผิด</u><br>
16.1 จนถึงขอบเขตสูงสุดที่กฎหมายที่เกี่ยวข้องอนุญาตไว้ ไม่ว่าในสถานการณ์ใดก็ตาม Moldii App จะไม่รับผิดไม่ว่าตามสัญญา การรับประกัน การละเมิด (รวมถึง แต่ไม่จำกัดเพียง ความประมาทเลินเล่อ (ไม่ว่าในเชิงรุก เชิงรับ หรือถูกใส่ความ) ความรับผิดของผลิตภัณฑ์ ความรับผิดโดยสิ้นเชิงหรือทฤษฎีอื่น) หรือประเด็นฟ้องอื่นตามกฎหมาย ตามความเที่ยงธรรม ตามบทกฎหมาย หรืออื่นใด ต่อ:
<br>
16.1.1 (ก) การสูญเสียการใช้งาน (ข) การสูญเสียกำไร (ค) การสูญเสียรายได้ (ง) การสูญเสียข้อมูล (จ) การสูญเสียค่าความนิยม หรือ (ฉ) การล้มเหลวในการรับรู้เงินออมที่คาดการณ์ไว้ ในแต่ละกรณีทั้งในทางตรงและทางอ้อม หรือ
<br>
16.1.2 ความเสียหายทางอ้อม โดยบังเอิญ พิเศษ หรือที่เป็นผลตามมา
ที่เกิดจากหรือเกี่ยวเนื่องกับการใช้หรือการไม่สามารถใช้ไซต์นี้หรือบริการ รวมถึงแต่ไม่จำกัดเพียง ความเสียหายอันเกิดจากสิ่งดังกล่าว แม้ว่า Moldii App จะได้รับแจ้งถึงความเป็นไปได้ของความเสียหายดังกล่าวแล้วก็ตาม
<br>
16.2 คุณรับทราบและตกลงว่าสิทธิเพียงอย่างเดียวที่คุณมีเกี่ยวกับปัญหาหรือความไม่พึงพอใจใดๆ ในบริการคือการขอให้ยกเลิกบัญชีของคุณ และ/หรือหยุดใช้งานบริการ
<br>
16.3 ถ้า โดยไม่คำนึงถึงความในส่วนก่อนหน้า Moldii App ถูกศาลหรือเขตอำนาจศาลที่มีอำนาจสั่งให้ต้องรับผิด (รวมถึงต่อการประมาทเลินเล่ออย่างร้ายแรง) จนถึงขอบเขตสูงสุดที่กฎหมายที่เกี่ยวข้องอนุญาตไว้ ความรับผิดต่อคุณหรือบุคคลภายนอกใดๆ จะจำกัดอยู่ที่น้อยกว่าสิ่งต่อไปนี้ (ก) จำนวนเงินที่ต้องชำระให้แก่คุณตาม บริษัท เพลินพอดี และ (ข) SG $100 (หนึ่งร้อยดอลลาร์สิงคโปร์)
<br>
16.4 ไม่มีสิ่งใดในเงื่อนไขการให้บริการนี้ที่จะจำกัดหรือยกเว้นความรับผิดต่อการเสียชีวิต หรือการบาดเจ็บร่างกายอันเกิดจากความประมาทเลินเล่อของ Moldii App การฉ้อโกง หรือต่อความรับผิดอื่นใดในส่วนของ Moldii App ที่ไม่สามารถจำกัดและ/หรือยกเว้นได้ตามกฎหมาย

