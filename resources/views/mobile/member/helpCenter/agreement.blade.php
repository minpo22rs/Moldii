<u>18. การมีส่วนร่วมของคุณต่อบริการ</u>
<br>
18.1 การส่งเนื้อหาเพื่อรวมไว้ในบริการ แสดงว่าคุณรับรองและรับประกันว่าคุณมีสิทธิและ/หรือการอนุญาตที่จำเป็นทั้งหมดเพื่อมอบสิทธิ์การใช้งานด้านล่างแก่ Moldii App คุณยังรับทราบและตกลงต่อไปว่า คุณคือผู้รับผิดชอบแต่เพียงผู้เดียวต่อสิ่งใดก็ตามที่คุณโพสต์หรือทำให้ใช้งานได้บนหรือผ่านทางบริการ รวมถึงแต่ไม่จำกัดเพียง ความถูกต้อง ความน่าเชื่อถือ ลักษณะ การปลอดจากสิทธิ์ การตรงตามกฎหมายและข้อจำกัดทางกฎหมาย ที่เกี่ยวข้องกับเนื้อหาที่ส่งเข้ามา คุณมอบสิทธิ์การใช้งานตลอดกาล ที่เพิกถอนไม่ได้ เป็นสากล ไม่ผูกขาด ปลอดค่าลิขสิทธิ์ สามารถมอบสิทธิต่อไปได้ และเปลี่ยนมือได้ ให้แก่ Moldii App และผู้รับช่วงต่อ ในการใช้ คัดลอก แจกจ่าย เผยแพร่ซ้ำ ส่งต่อ ดัดแปลง ปรับเปลี่ยน สร้างงานอนุพันธ์จาก แสดงแก่สาธารณะ และดำเนินการต่อสาธารณะ เนื้อหาที่ส่งเข้ามาดังกล่าวบน ผ่านทางหรือที่เกี่ยวเนื่องกับบริการ ในรูปแบบสื่อใดๆ และผ่านช่องทางสื่อใด รวมถึงแต่ไม่จำกัดเพียง เพื่อการส่งเสริมการขายและแจกจ่ายต่อส่วนต่างๆ ของบริการ (และงานอนุพันธ์ของบริการ) โดยไม่ต้องมีอำนาจหน้าที่ที่ได้รับมอบหมาย และคุณตกลงที่จะสละสิทธิทางศีลธรรมใดๆ (และสิทธิที่คล้ายกันในส่วนต่างๆ ทั่วโลก) ในเรื่องดังกล่าว คุณเข้าใจว่าสิ่งที่คุณส่งเข้ามานั้นอาจถูกส่งต่อทางเครือข่ายต่างๆ และถูกเปลี่ยนเพื่อให้ตรงตามและปรับให้เข้ากับข้อกำหนดทางเทคนิค
<br>
18.2 วัสดุ ข้อมูล หรือแนวคิดใดที่คุณโพสต์บนหรือผ่านทางบริการ หรือส่งต่อมายัง Moldii App ไม่ว่าด้วยวิธีใด (เรียกว่า “สิ่งที่ส่งมา”) ไม่ถือว่าเป็นความลับโดย Moldii App และอาจถูกเผยแพร่หรือใช้โดย Moldii App โดยปราศจากการชดเชยหรือการรับผิดต่อคุณ ไม่ว่าด้วยจุดประสงค์ใดก็ตาม รวมถึงแต่ไม่จำกัดเพียง การพัฒนา การผลิต และการทำตลาดผลิตภัณฑ์ ในการส่งสิ่งที่ส่งมาแก่ Moldii App คุณรับทราบและตกลงว่า Moldii App และ/หรือบุคคลภายนอกอื่นๆ อาจพัฒนาซอฟต์แวร์ แอปพลิเคชัน อินเทอร์เฟส ผลิตภัณฑ์ และส่วนปรับปรุงของสิ่งดังกล่าวโดยอิสระ ซึ่งมีลักษณะการทำงาน รหัส หรือลักษณะพิเศษอื่นตรงหรือคล้ายกับแนวคิดที่กำหนดไว้ในสิ่งที่ส่งมาของคุณ ดังนั้นคุณจึงได้มอบสิทธิ์การใช้งานตลอดกาล ที่เพิกถอนไม่ได้ เป็นสากล ไม่ผูกขาด ปลอดค่าลิขสิทธิ์ สามารถมอบสิทธิต่อไปได้ และเปลี่ยนมือได้ ให้แก่ Moldii App และผู้รับช่วงต่อ ในการพัฒนาสิ่งที่ระบุไว้ข้างตน และเพื่อใช้ คัดลอก แจกจ่าย เผยแพร่ซ้ำ ส่งต่อ ดัดแปลง ปรับเปลี่ยน สร้างงานอนุพันธ์จาก แสดงแก่สาธารณะ และดำเนินการต่อสาธารณะ สิ่งที่ส่งมาดังกล่าวบน ผ่านทางหรือที่เกี่ยวเนื่องกับบริการ ในรูปแบบสื่อใดๆ และผ่านช่องทางสื่อใด รวมถึงแต่ไม่จำกัดเพียง เพื่อการส่งเสริมการขายและแจกจ่ายต่อส่วนต่างๆ ของบริการ (และงานอนุพันธ์ของบริการ) โดยไม่ต้องมีอำนาจหน้าที่ที่ได้รับมอบหมาย และคุณตกลงที่จะสละสิทธิทางศีลธรรมใดๆ (และงานอนุพันธ์จากบริการ) ข้อกำหนดนี้ไม่ใช้บังคับกับข้อมูลส่วนบุคคลที่ต้องอยู่ภายใต้นโยบายความเป็นส่วนตัวของเรา เว้นแต่ในขอบเขตที่คุณทำให้ข้อมูลส่วนบุคคลดังกล่าวใช้งานได้แก่สาธารณะบนหรือผ่านทางบริการ


