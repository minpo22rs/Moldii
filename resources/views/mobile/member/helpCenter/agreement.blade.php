19.1 ผู้มีส่วนร่วมในบริการแต่ละคน ในด้านข้อมูล ข้อความ ภาพ เสียง วิดีโอ ซอฟต์แวร์ และเนื้อหาอื่นๆ เป็นผู้รับผิดชอบแต่เพียงผู้เดียวต่อความถูกต้อง ความน่าเชื่อถือ ลักษณะ การปลอดจากสิทธิ์ การตรงตามกฎหมายและข้อจำกัดทางกฎหมาย ที่เกี่ยวข้องกับเนื้อหาที่ส่งเข้ามา ดังนั้น Moldii App จะไม่รับผิดชอบต่อ และจะไม่เฝ้าติดตามหรือตรวจสอบอย่างสม่ำเสมอถึงความถูกต้อง ความน่าเชื่อถือ ลักษณะ การปลอดจากสิทธิ์ การตรงตามกฎหมายและข้อจำกัดทางกฎหมาย ที่เกี่ยวข้องกับเนื้อหาที่ส่งเข้ามา คุณจะไม่กำหนดให้ Moldii App ต้องรับผิดชอบต่อการดำเนินการหรือไม่ดำเนินการของผู้ใช้ รวมถึงแต่ไม่จำกัดเพียง สิ่งที่โพสต์หรือทำให้ใช้งานได้ผ่านทางบริการ
<br>
19.2 นอกจากนี้ บริการอาจมีลิงก์ไปยังผลิตภัณฑ์ เว็บไซต์ บริการและข้อเสนอของบุคคลภายนอก ลิงก์ ผลิตภัณฑ์ เว็บไซต์ บริการของบุคคลภายนอกเหล่านี้ไม่ได้เป็นของหรือควบคุมโดย Moldii App แต่ดำเนินการโดย และเป็นทรัพย์สินของบุคคลภายนอกที่เกี่ยวข้อง และอาจได้รับการคุ้มครองตามกฎหมายลิขสิทธิ์หรือกฎหมายและสนธิสัญญาว่าด้วยทรัพย์สินทางปัญญาอื่นๆ ที่บังคับใช้ Moldii App ไม่ได้ตรวจสอบ และไม่รับผิดชอบต่อเนื้อหา ฟังก์ชั่นการทำงาน ความปลอดภัย บริการ นโยบายความเป็นส่วนตัว หรือการปฏิบัติอื่นๆ ของบุคคลภายนอกเหล่านี้ ขอแนะนำให้อ่านข้อกำหนดและนโยบายอื่นๆ ที่เผยแพร่บนเว็บไซต์หรืออื่นๆ โดยบุคคลภายนอกดังกล่าว ในการใช้บริการ คุณตกลงว่า Moldii App จะไม่รับผิดชอบในลักษณะใดก็ตามอันเนื่องมาจากการใช้งานของคุณหรือการไม่สามารถใช้งานเว็บไซต์หรือวิดเจ็ตใดๆ คุณรับทราบและตกลงต่อไปด้วยว่า Moldii App อาจยกเลิกการใช้งานของคุณ หรือลบลิงก์ของบุคคลภายนอก หรือแอปพลิเคชันบนบริการ ตามขอบเขตที่การใช้งานนั้นละเมิดเงื่อนไขการให้บริการนี้









