<u>17. ลิงก์ไปยังไซต์ของบุคคลภายนอกและแชร์วิดีโอจาก YouTube</u>
<br>
17.1 ลิงก์ของบุคคลภายนอกซึ่งมีอยู่ทั่วทั้งไซต์ จะให้คุณสามารถออกจากไซต์นี้ได้ ลิงก์เหล่านี้จัดไว้ให้ตามมารยาทเท่านั้น และไซต์ที่มีลิงก์ไปไม่ได้อยู่ภายใต้การควบคุมของ Moldii App ไม่ว่าในลักษณะใด และคุณเข้าถึงไซต์เหล่านี้ด้วยความเสี่ยงของคุณเอง Moldii App ไม่รับผิดชอบไม่ว่ากรณีใดต่อเนื้อหาของไซต์ที่ลิงก์ไปดังกล่าว หรือลิงก์ที่อยู่ในไซต์ที่มีลิงก์ไป รวมถึงการเปลี่ยนแปลงหรือการอัปเดตไซต์ดังกล่าว Moldii App จัดลิงก์เหล่านี้ไว้ให้เพื่อความสะดวกเท่านั้น และการรวมลิงก์ใดๆ ไว้ไม่ได้แสดงนัยหรือการเชื่อมโยงอย่างชัดแจ้ง การรับรอง หรือการเป็นผู้สนับสนุนโดย Moldii App ถึงไซต์ที่ลิงก์ไปใดๆ และ/หรือเนื้อหาในไซต์ที่ลิงก์ไปนั้น
<br>
17.2 Moldii App อนุญาตให้คุณสามารถแชร์วิดีโอจาก YouTube ใน Moldii App (“เนื้อหาของ YouTube”) โดยการแชร์เนื้อหาของ YouTube คุณตกลงผูกพันที่จะปฏิบัติตามเงื่อนไขการให้บริการของ YouTube 
















