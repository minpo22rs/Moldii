<u>24. บทบัญญัติทั่วไป</u>
<br>
24.1 Moldii App สงวนสิทธิ์ทั้งหมดที่ไม่ได้ให้ไว้โดยชัดแจ้งในที่นี้
<br>
24.2 Moldii App อาจแก้ไขเงื่อนไขการให้บริการนี้ได้ตลอดเวลาโดยโพสต์เงื่อนไขการให้บริการฉบับแก้ไขบนไซต์นี้ การใช้งานไซต์นี้ต่อไปของคุณหลังการเปลี่ยนแปลงดังกล่าวจะถือว่าคุณยอมรับเงื่อนไขการให้บริการฉบับแก้ไขนั้นแล้ว
<br>
24.3 คุณต้องไม่มอบหมาย ให้สิทธิ์การใช้งานต่อ หรือโอนสิทธิที่มอบให้แก่คุณในที่นี้หรือทำสัญญาช่วงข้อผูกพันใดๆ ของคุณ
<br>
24.4 ไม่มีสิ่งใดในเงื่อนไขการให้บริการนี้จะถือเป็นการเป็นหุ้นส่วน การลงทุนร่วม หรือความสัมพันธ์ระหว่างตัวการกับตัวแทน ระหว่างคุณและ Moldii App และไม่มีสิ่งใดอนุญาตให้คุณสร้างภาระค่าใช้จ่ายหรือหนี้สินในนามของ Moldii App
<br>
24.5 ความล้มเหลวของ Moldii App ไม่ว่าเวลาใด ที่จะบังคับใช้บทบัญญัติใดของเงื่อนไขนี้ ไม่ส่งผลไม่ว่าในทางใดต่อสิทธิของตนในภายหลังที่จะบังคับใช้บทบัญญัติดังกล่าว เว้นแต่มีการสละสิทธิ์ไว้เป็นลายลักษณ์อักษร
<br>
24.6 เงื่อนไขการให้บริการนี้มีไว้เพื่อคุณและประโยชน์ของเราเท่านั้น และไม่ใช่เพื่อประโยชน์ของบุคคลหรือนิติบุคคลอื่นใด เว้นแต่บริษัทย่อยและบริษัทในเครือของ Moldii App (และผู้รับช่วงและผู้รับมอบหมายของบริษัทย่อยและบริษัทในเครือที่เกี่ยวข้องของ Moldii App แต่ละแห่ง)
<br>
24.7 เงื่อนไขที่กำหนดไว้ในเงื่อนไขการให้บริการนี้และข้อตกลงรวมถึงนโยบายใดๆ ที่รวมอยู่ในหรืออ้างถึงในเงื่อนไขการให้บริการนี้ ถือเป็นข้อตกลงที่สมบูรณ์ และความเข้าใจของคู่สัญญาอันเกี่ยวกับบริการและไซต์ และใช้แทนที่ข้อตกลงหรือความเข้าใจก่อนหน้านี้ระหว่างคู่สัญญาซึ่งเกี่ยวข้องกับเรื่องดังกล่าว ดังนั้นคู่สัญญาจึงยกเว้นเงื่อนไขโดยนัยทั้งหมดตามข้อเท็จจริง ในการเข้าทำข้อตกลงที่เกิดจากเงื่อนไขการให้บริการนี้นั้น คู่สัญญาไม่ได้พึ่งพาถ้อยแถลง การรับรอง การรับประกัน ความเข้าใจ ภาระหน้าที่ คำสัญญา หรือคำมั่นจากบุคคลใดนอกเหนือจากที่ระบุไว้อย่างชัดแจ้งในเงื่อนไขการให้บริการนี้ คู่สัญญาแต่ละฝ่ายสละการเรียกร้องที่เพิกถอนไม่ได้และไม่มีเงื่อนไข สิทธิและการเยียวยาทั้งหมด ซึ่งแต่สำหรับส่วนนี้ ตนอาจมีอันเกี่ยวเนื่องกับสิ่งใดที่กล่าวมาก่อนหน้านี้ เงื่อนไขการให้บริการนี้ไม่อาจโต้แย้ง อธิบาย หรือเสริมด้วยหลักฐานข้อตกลงก่อนหน้า ข้อตกลงปากเปล่าซึ่งเกิดขึ้นพร้อมกัน หรือเงื่อนไขเพิ่มเติมที่สอดคล้องกันใดๆ
