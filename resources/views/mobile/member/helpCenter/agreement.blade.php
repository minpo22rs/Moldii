<u>21. การกระทำอันเป็นการฉ้อโกงหรือการกระทำที่ต้องสงสัย</u>
<br>
โดยใช้ดุลยพินิจของ Moldii App แต่เพียงผู้เดียว หาก Moldii App มีเหตุอันควรเชื่อได้ว่าคุณได้กระทำการหรือทำธุรกรรมใดๆ ที่อาจเป็นการฉ้อโกงหรือเป็นที่ต้องสงสัย เราอาจจะดำเนินมาตรการต่างๆ เพื่อปกป้อง Moldii App ผู้ซื้อหรือผู้ขายคนอื่นๆ หรือบุคคลภายนอกจากการคืนเงิน (reversals) การปฏิเสธการจ่ายเงิน (chargebacks) การคัดค้านการจ่ายเงิน (claims) ค่าธรรมเนียม ค่าปรับ เบี้ยปรับและความรับผิดอื่นใด มาตรการที่เราอาจจะดำเนินการได้แก่
<br>
(ก) เราอาจจะปิด ระงับหรือจำกัดการเข้าถึงบัญชีของคุณหรือการให้บริการ และ/หรือระงับการทำธุกรรมใดๆ<br>
(ข) เราอาจจะระงับสิทธิของคุณสำหรับ บัญชี<br>
<br>(ค) เราอาจจะถือครองไว้ ใช้หรือโอนเงินที่อยู่ในบัญชีของคุณตามคำพิพากษาและคำสั่งที่เกี่ยวข้องกับคุณหรือบัญชีของคุณ ซึ่งรวมถึงคำพิพากษาและคำสั่งของศาลของประเทศสิงคโปร์หรือที่อื่นใดที่เกี่ยวข้องกับ Moldii App
(ง) เราอาจปฏิเสธการให้บริการแก่คุณทันทีหรือในอนาคต<br>
<br>(จ) เราอาจจะถือครองเงินของท่านในระยะเวลาที่จำเป็นต่อการป้องกันความเสี่ยงที่อาจก่อให้เกิดความรับผิดต่อ Moldii App หรือบุคคลภายนอก หรือกรณีที่เรามีความเชื่อมั่นว่าคุณได้กระทำการหรือทำธุรกรรมใดๆ ที่อาจเป็นการฉ้อโกงหรือเป็นที่ต้องสงสัย
<br>
เพื่อวัตถุประสงค์ในการตีความในข้อนี้
<br>“การปฏิเสธการจ่ายเงิน (chargebacks)” หมายถึง คำร้องเรียนที่ผู้ซื้อยื่นต่อบริษัทบัตรเดบิตหรือบัตรเครดิตของผู้ซื้อหรือธนาคารที่ออกบัตรเดบิตหรือบัตรเครดิตเพื่อทำให้การจ่ายเงินเป็นโมฆะ
<br>“การคัดค้านการจ่ายเงิน (claims)” หมายถึง การคัดค้านการจ่ายเงินที่ผู้ซื้อหรือผู้ขายได้แจ้งต่อ Moldii App โดยตรง
<br>“การคืนเงิน (reversals)” หมายถึง การคืนเงินค่าสินค้าโดย Moldii App เนื่องจาก (ก) ธนาคารของผู้ร้องขอได้ทำให้การจ่ายเงินดังกล่าวเป็นโมฆะ (ข) Moldii App ได้นำส่งค่าสินค้าแก่คุณโดยผิดพลาด (ค) ผู้นำส่งค่าสินค้าไม่ได้รับอนุญาตให้นำส่งค่าสินค้าดังกล่าว (ตัวอย่างเช่น ผู้นำส่งได้ใช้บัตรเครดิตที่ถูกขโมย) (ง) คุณได้รับค่าสินค้าจากการกระทำที่เป็นอันฝ่าฝืนข้อกำหนดการให้บริการนี้ หรือ (จ) Moldii App ประสงค์ที่จะคัดค้านการจ่ายเงินกับคุณ



