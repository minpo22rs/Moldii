<u>22. การชดใช้ค่าเสียหาย</u>
<br>
22.1 คุณตกลงที่จะชดใช้ ปกป้องและทำให้ Moldii App ผู้ถือหุ้น บริษัทย่อย บริษัทในเครือ กรรมการ เจ้าหน้าที่ ตัวแทน ผู้ร่วมแบรนด์ หรือหุ้นส่วนอื่น และพนักงาน (เรียกรวมว่า “ฝ่ายที่ได้รับการชดใช้”) พ้นจากการเรียกร้องสิทธิ การดำเนินการ การดำเนินคดี และการฟ้องร้องคดี รวมถึงหนี้สิน ความเสียหาย การชำระหนี้ บทลงโทษ ค่าปรับ ค่าใช้จ่ายและต้นทุนที่เกี่ยวข้องทั้งหมด (รวมถึงแต่ไม่จำกัดเพียง ค่าใช้จ่ายในการยุติข้อพิพาทอื่นๆ) ที่เกิดแก่ฝ่ายที่ได้รับการชดใช้ อันเกิดจากหรือเกี่ยวข้องกับ: (ก) ธุรกรรมใดๆ ที่ทำขึ้นบนไซต์ หรือข้อพิพาทใดๆ ที่เกี่ยวข้องกับธุรกรรมดังกล่าว เว้นแต่กรณีที่ Moldii App หรือบริษัทในเครือเป็นผู้จำหน่ายสินค้าในธุรกรรมที่เกี่ยวข้องกับข้อพิพาทดังกล่าว) (ข) Moldii App (ค) การโฮสต์ การดำเนินงาน การจัดการและ/หรือการดูแลระบบบริการ โดยหรือในนามของ Moldii App (ง) การฝ่าฝืนหรือละเมิดเงื่อนไขใดๆ ในเงื่อนไขการให้บริการนี้และนโยบายหรือแนวทางปฏิบัติอื่นใดที่อ้างถึงในที่นี่ (จ) การใช้หรือใช้บริการในทางที่ผิด หรือ (ฉ) การละเมิดกฎหมายหรือสิทธิใดๆ ของบุคคลภายนอก

















